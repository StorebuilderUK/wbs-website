// Helper function to get blog image
export function getBlogImage(heroImage: string | undefined): any {
  if (!heroImage) return null;
  
  // If it's already a full URL (external image), return as-is
  if (heroImage.startsWith('http')) return heroImage;
  
  // Otherwise, get from our imported images
  return blogImages[heroImage] || null;
}